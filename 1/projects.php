        <h2>The Beginning...</h2>
		
        <br/>
		<p> The nimbus of Badhan was started from the tiny corner of Shahidullah Hall of DU in 1997 to make voluntary fresh blood 
donation as a Social Movement.Badhan is a voluntary blood donars' organization. The journey of Badhan has  been continuing
since 1997. Through a Free  Blood Group Testing Program on 24 October, 1997 in the Shahidullah Hall of Dhaka University,
Badhan expressed itself. Before that,dream of an organization from the thoughts of doing something for the people requiring 
blood(specillay who gathered regularly in the Hall Gate)was running in the mind of one student of that hall Md. Shahidul
Islam Ripon. Later he pioneered some other students along with him toward the same thought.And the outcome was that 24 
October's program and Badhan! We, the members of Badhan, donate blood ourselves and motivate others also to donate blood!
We believe one's blood can save another one life!.</p>	
Establishment Date : 24 October,1997.
 Govt. Registration No: DHA-06152

 Nature of the Organization: It is totally non-political, non-communal,
 non- regional, non-racial, secular and voluntary social organization.

Goals and objectives: Initiating social movement to build up a healthy
 society through motivating voluntary blood donation, donating blood 
 voluntarily and others services and awareness programs.
 <h2> Name : </h2>'BADHAN' (A Voluntary Blood Donors' Organization).‘বাঁধন’ (স্বেচ্ছায় রক্তদাতাদের সংগঠন).
<h2> Slogan:</h2>‘ একের রক্ত অন্যের জীবন, রক্তই হোক আত্মার বাঁধন ‘.
<h2>Programs:</h2>
